void Ble_app_init(void)
{
    ret_code_t err_code;
    ble_stack_init();
    gap_params_init();
    gatt_init();
    services_init();
    advertising_init();
    conn_params_init();
    
    err_code = app_timer_create(&m_blink_ble, APP_TIMER_MODE_REPEATED, blink_handler);
    APP_ERROR_CHECK(err_code);
    err_code = app_timer_create(&m_heart_rate,APP_TIMER_MODE_REPEATED,heart_rate_handler);
    APP_ERROR_CHECK(err_code);	
    // Create a FreeRTOS task for the BLE stack.
    // The task will run advertising_start() before entering its loop.
    nrf_sdh_freertos_init(advertising_start, NULL);
}
